    private void btnCadastrarVendedorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarVendedorActionPerformed

        Validador objValidador = new Validador();
        objValidador.ValidarTexto(txtNomeUsuario);
        objValidador.ValidarTexto(txtLoginUsuario);
        objValidador.ValidarTexto(txtSenha);

        if (objValidador.hasErro()) {
            JOptionPane.showMessageDialog(rootPane, objValidador.getMensagensErro());
        } else {

            String nome = (txtNomeUsuario.getText());
            String login = (txtLoginUsuario.getText());
            String captura = new String(txtSenha.getPassword());
            String senha = captura;

            Usuario novoUsuario = new Usuario(nome, login, senha);

            boolean retorno = UsuarioDAO.cadastrarUsuario(novoUsuario);

            if (retorno == true) {
                JOptionPane.showMessageDialog(rootPane, "Cadastro efetuado");
                txtLoginUsuario.setText("");
                txtNomeUsuario.setText("");
                txtSenha.setText("");
            } else {
                JOptionPane.showMessageDialog(rootPane, "Falha!");
            }

        }
        objValidador.limparMensagens();

    }//GEN-LAST:event_btnCadastrarVendedorActionPerformed

